week_no= int(input("Enter the week no"))
if week_no == 1:
    print("Monday")
elif week_no == 2:
    print("Tuesday")
elif week_no == 3:
    print("Wednesday")
elif week_no == 4:
    print("Thursday")
elif week_no == 5:
    print("Friday")
elif week_no == 6:
    print("Saturday")
elif week_no == 7:
    print("Sunday")
else:
    print("Invalid")